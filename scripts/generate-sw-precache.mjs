// scripts/generate-sw-precache.mjs
// ─────────────────────────────────────────────────────────────────────────────
// postbuild: emit public/sw-precache.js — the build-time precache manifest the
// service worker imports. See docs/offline-shell.md.
//
// Source of truth: the PRERENDERED HTML of each shell route
// (.next/server/app/<route>.html). Parsing those for /_next/static references
// is bundler-agnostic (this repo's build emits no app-build-manifest.json) and
// exact by construction — the asset list is literally what the cached shell
// HTML will request. Pure core (extractAssets/buildPrecache) is unit-tested;
// the CLI tail wires it to .next/ and public/.
// ─────────────────────────────────────────────────────────────────────────────

import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

// The statically-prerendered routes that make up the offline shell.
// /library/[slug] pages are deliberately excluded (see design note).
export const SHELL_ROUTES = ["/", "/session", "/profile", "/performance", "/library"];

// "/" → index.html, "/session" → session.html, …
export const htmlFileFor = (route) => (route === "/" ? "index.html" : `${route.slice(1)}.html`);

// Strict charset: stops at quotes/backslashes, so escaped-JSON duplicates
// inside inlined RSC payloads don't produce phantom `foo.js\` entries.
const ASSET_RE = /\/_next\/static\/[A-Za-z0-9_\-./]+?\.(?:js|css)/g;

/** Pure: unique /_next/static asset paths referenced by an HTML document. */
export function extractAssets(html) {
  return [...new Set(html.match(ASSET_RE) || [])];
}

/**
 * Pure core: manifest object from { route: htmlString } + build id.
 * Routes whose HTML is missing (null/undefined) are reported in
 * missingRoutes — the CLI fails the build on any, so a renamed shell route
 * can't silently thin the precache.
 */
export function buildPrecache(htmlByRoute, buildId) {
  const assets = new Set();
  const missingRoutes = [];
  for (const route of SHELL_ROUTES) {
    const html = htmlByRoute[route];
    if (typeof html !== "string") { missingRoutes.push(route); continue; }
    for (const a of extractAssets(html)) assets.add(a);
  }
  return { version: buildId, shellRoutes: SHELL_ROUTES, assets: [...assets].sort(), missingRoutes };
}

export function renderPrecacheModule({ version, shellRoutes, assets }) {
  return `// GENERATED by scripts/generate-sw-precache.mjs (postbuild) — do not edit.\nself.__FORGE_PRECACHE = ${JSON.stringify({ version, shellRoutes, assets }, null, 2)};\n`;
}

// ─── CLI ───────────────────────────────────────────────────────────────────
const invokedDirectly = process.argv[1] && import.meta.url.endsWith(process.argv[1].split("/").pop());
if (invokedDirectly) {
  const root = process.cwd();
  const htmlByRoute = {};
  for (const route of SHELL_ROUTES) {
    try {
      htmlByRoute[route] = readFileSync(resolve(root, ".next/server/app", htmlFileFor(route)), "utf8");
    } catch { /* left missing → reported below */ }
  }
  const buildId = readFileSync(resolve(root, ".next/BUILD_ID"), "utf8").trim();
  const manifest = buildPrecache(htmlByRoute, buildId);
  if (manifest.missingRoutes.length) {
    console.error(`[sw-precache] no prerendered HTML for shell routes: ${manifest.missingRoutes.join(", ")} — route renamed/removed, or no longer static?`);
    process.exit(1);
  }
  if (manifest.assets.length === 0) {
    console.error("[sw-precache] extracted zero assets — extraction regex no longer matches this Next output?");
    process.exit(1);
  }
  writeFileSync(resolve(root, "public/sw-precache.js"), renderPrecacheModule(manifest));
  console.log(`[sw-precache] build ${manifest.version}: ${manifest.assets.length} assets, ${manifest.shellRoutes.length} shell routes`);
}
